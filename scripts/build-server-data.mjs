// Emit build-time data for server bundle:
//   server/generated/labs-data.mjs       — labs (from fixtures/labs/*.json)
//   server/generated/migrations-data.mjs — all SQL migrations in order
//
// Runs before esbuild on CI and before `npm run dev:server` locally.

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname, basename } from 'node:path';
import { createHash } from 'node:crypto';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const fixturesDir = resolve(projectRoot, 'fixtures', 'labs');
const migrationsDir = resolve(projectRoot, 'server', 'db', 'migrations');
const outDir = resolve(projectRoot, 'server', 'generated');

mkdirSync(outDir, { recursive: true });

// ── Labs (from fixtures/labs/*.json — schema v3 THINK/SEE/SHIP) ─────────────
const labs = [];
for (const f of readdirSync(fixturesDir).filter((x) => x.endsWith('.json')).sort()) {
  const raw = readFileSync(resolve(fixturesDir, f), 'utf8');
  const data = JSON.parse(raw);
  labs.push({
    slug: data.slug || basename(f, '.json'),
    module: data.module || 'unknown',
    title: String(data.title || data.slug || basename(f, '.json')),
    file_path: data.file_path || `fixtures/labs/${f}`,
    tldr: data.tldr || [],
    walkthrough: data.walkthrough || [],
    quiz: data.quiz || [],
    flashcards: data.flashcards || [],
    tryAtHome: data.tryAtHome || data.try_at_home || [],
    estimatedMinutes: Number.isFinite(data.estimated_minutes) ? data.estimated_minutes
      : Number.isFinite(data.estimatedMinutes) ? data.estimatedMinutes : null,
    content_hash: data.content_hash || createHash('sha1').update(raw).digest('hex'),
  });
}

writeFileSync(
  resolve(outDir, 'labs-data.mjs'),
  `// AUTO-GENERATED by scripts/build-server-data.mjs — do not edit.\nexport const labs = ${JSON.stringify(labs)};\n`
);
console.log(`[build-server-data] wrote labs-data.mjs (${labs.length} labs from fixtures)`);

// ── Migrations ──────────────────────────────────────────────────────────────
const migrationFiles = readdirSync(migrationsDir).filter((f) => f.endsWith('.sql')).sort();
const migrations = migrationFiles.map((name) => ({
  name,
  sql: readFileSync(resolve(migrationsDir, name), 'utf8'),
}));

writeFileSync(
  resolve(outDir, 'migrations-data.mjs'),
  `// AUTO-GENERATED by scripts/build-server-data.mjs — do not edit.\nexport const migrations = ${JSON.stringify(migrations)};\n`
);
console.log(`[build-server-data] wrote migrations-data.mjs (${migrations.length} migrations)`);
