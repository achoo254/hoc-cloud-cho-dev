// Emit build-time data for server bundle:
//   server/generated/labs-data.mjs       — all labs HTML (slug, module, file_path, html)
//   server/generated/migrations-data.mjs — all SQL migrations in order
//
// Runs on CI before esbuild bundles the server. Also runs before `npm run dev`
// so the unbundled server can import the same generated modules.

import { readdirSync, readFileSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { resolve, relative, basename, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const labsDir = resolve(projectRoot, 'labs');
const migrationsDir = resolve(projectRoot, 'server', 'db', 'migrations');
const outDir = resolve(projectRoot, 'server', 'generated');

mkdirSync(outDir, { recursive: true });

// ── Labs ────────────────────────────────────────────────────────────────────
function walkHtml(dir) {
  const out = [];
  for (const ent of readdirSync(dir, { withFileTypes: true })) {
    const full = resolve(dir, ent.name);
    if (ent.isDirectory()) {
      if (ent.name.startsWith('_') || ent.name === 'node_modules') continue;
      out.push(...walkHtml(full));
    } else if (ent.isFile() && ent.name.endsWith('.html') && ent.name !== 'index.html') {
      out.push(full);
    }
  }
  return out;
}

const labs = [];
if (statSync(labsDir, { throwIfNoEntry: false })) {
  for (const file of walkHtml(labsDir)) {
    const rel = relative(projectRoot, file).replace(/\\/g, '/');
    labs.push({
      slug: basename(file, '.html').replace(/^\d+-/, ''),
      module: rel.split('/')[1] || 'unknown',
      file_path: rel,
      html: readFileSync(file, 'utf8'),
    });
  }
}

writeFileSync(
  resolve(outDir, 'labs-data.mjs'),
  `// AUTO-GENERATED by scripts/build-server-data.mjs — do not edit.\nexport const labs = ${JSON.stringify(labs)};\n`
);
console.log(`[build-server-data] wrote labs-data.mjs (${labs.length} labs)`);

// ── Migrations ──────────────────────────────────────────────────────────────
const migrationFiles = readdirSync(migrationsDir).filter((f) => f.endsWith('.sql')).sort();
const migrations = migrationFiles.map((name) => ({
  name,
  sql: readFileSync(resolve(migrationsDir, name), 'utf8'),
}));

writeFileSync(
  resolve(outDir, 'migrations-data.mjs'),
  `// AUTO-GENERATED by scripts/build-server-data.mjs — do not edit.\nexport const migrations = ${JSON.stringify(migrations)};\n`
);
console.log(`[build-server-data] wrote migrations-data.mjs (${migrations.length} migrations)`);
